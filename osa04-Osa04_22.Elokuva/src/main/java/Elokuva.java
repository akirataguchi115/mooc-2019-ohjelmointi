
public class Elokuva {

    private String nimi;
    private int ikaraja;

    public Elokuva(String nimi, int ikaraja) {
        this.nimi = nimi;
        this.ikaraja = ikaraja;
    }

    public String nimi() {
        return nimi;
    }

    public int ikaraja() {
        return ikaraja;
    }

}
