
public class Musiikkikappale {

    private String nimi;
    private int pituus;

    public Musiikkikappale(String nimi, int pituus) {
        this.nimi = nimi;
        this.pituus = pituus;
    }

    public String nimi() {
        return nimi;
    }

    public int pituus() {
        return pituus;
    }
}
